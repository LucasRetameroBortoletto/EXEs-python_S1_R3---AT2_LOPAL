listaMain = [5, 7, 2, 9, 4, 1, 3]

# testando algumas funções nativas de lista do python
print("O tamanho da lista é: .............{}".format(len(listaMain)))
print("O maior valor da lista é: .........{}".format(max(listaMain)))
print("O menor valor da lista é: .........{}".format(min(listaMain)))
print("A soma dos elementos da lista é:...{}".format(sum(listaMain)))
print("A lista em ordem crescente é: .....{}".format(sorted(listaMain)))
print("A lista em ordem decrescente é: ...{}".format(sorted(listaMain, reverse=True)))